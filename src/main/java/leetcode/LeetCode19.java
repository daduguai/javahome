package leetcode;
/**给你一个链表，删除链表的倒数第 n 个结点，并且返回链表的头结点。

 进阶：你能尝试使用一趟扫描实现吗*/
public class LeetCode19 {
    public ListNode removeNthFromEnd(ListNode head, int n) {
        ListNode node=new ListNode();
        if(head==null||head.next==null){
            return head;
        }

        return node;
    }
}
